public class THONGBAO {
	private int IdThongBao, HienThi;
	private String TieuDe, NoiDung, GuiDen, NgayDang;
	public int getIdThongBao() {
		return IdThongBao;
	}
	public void setIdThongBao(int idThongBao) {
		IdThongBao = idThongBao;
	}
	public int getHienThi() {
		return HienThi;
	}
	public void setHienThi(int hienThi) {
		HienThi = hienThi;
	}
	public String getTieuDe() {
		return TieuDe;
	}
	public void setTieuDe(String tieuDe) {
		TieuDe = tieuDe;
	}
	public String getNoiDung() {
		return NoiDung;
	}
	public void setNoiDung(String noiDung) {
		NoiDung = noiDung;
	}
	public String getGuiDen() {
		return GuiDen;
	}
	public void setGuiDen(String guiDen) {
		GuiDen = guiDen;
	}
	public String getNgayDang() {
		return NgayDang;
	}
	public void setNgayDang(String ngayDang) {
		NgayDang = ngayDang;
	}
}
